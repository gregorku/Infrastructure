#!/usr/bin/env bash

###############################################################################
#
# Infrastructure Project
#
# File:
#   scripts/lib/logging.sh
#
# Description:
#   Common logging functions used by all Infrastructure scripts.
#
###############################################################################

###############################################################################
# ANSI colors
###############################################################################

if [[ -t 1 ]]; then

    COLOR_RESET='\033[0m'

    COLOR_RED='\033[0;31m'

    COLOR_GREEN='\033[0;32m'

    COLOR_YELLOW='\033[1;33m'

    COLOR_BLUE='\033[0;34m'

else

    COLOR_RESET=''

    COLOR_RED=''

    COLOR_GREEN=''

    COLOR_YELLOW=''

    COLOR_BLUE=''

fi

###############################################################################
# Internal logging functions
###############################################################################

log_info() {

    printf "${COLOR_BLUE}[INFO]${COLOR_RESET} %s\n" "$*"
}

log_success() {

    printf "${COLOR_GREEN}[ OK ]${COLOR_RESET} %s\n" "$*"
}

log_warn() {

    printf "${COLOR_YELLOW}[WARN]${COLOR_RESET} %s\n" "$*"
}

log_error() {

    printf "${COLOR_RED}[FAIL]${COLOR_RESET} %s\n" "$*" >&2
}

###############################################################################
# Separator
###############################################################################

separator()
{
    printf '%80s\n' '' | tr ' ' '-'
}

###############################################################################
# Sections
###############################################################################

log_step() {

    separator

    printf "%s\n" "$*"

    separator
}

###############################################################################
# Public API
###############################################################################

info() {

    log_info "$@"
}

ok() {

    log_success "$@"
}

warn() {

    log_warn "$@"
}

fail() {

    log_error "$@"

    exit 1
}

###############################################################################
# Headers
###############################################################################

print_section() {

    log_step "$@"
}

#
# Backward compatibility
#

print_header() {

    print_section "$@"
}

###############################################################################
# Footer
###############################################################################

print_footer()
{
    ok "$@"
}